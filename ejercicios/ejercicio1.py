'''
Ejercicio 1: Operadores Aritméticos

Problema: Escribe un programa que solicite al usuario dos números y 
realice las operaciones de suma, resta, multiplicación, división y módulo.

'''

numero_1 =  float(input("ingrese un numero"))

numero_2 =  float(input("ingrese un numero 2"))

#suma

print("el resultado de la suma es:", numero_1 + numero_2)


#resta

print("el resultado de la resta es:", numero_1 - numero_2)


#multiplicacion
print("el resultado de la multiplicacion es:",numero_1 * numero_2)

#mdivision
print("el resultado de la division es:",numero_1 / numero_2)

#modulo
print("el resultado de modulo es:",numero_1 % numero_2)
